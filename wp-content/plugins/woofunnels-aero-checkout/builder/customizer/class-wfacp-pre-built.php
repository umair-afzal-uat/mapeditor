						return;
					}
				}
			}

			if ( is_array( $breadcrumb ) && count( $breadcrumb ) > 0 ) {

				$steps_arr = [ 'single_step', 'two_step', 'third_step' ];

				$breadcrumb = $this->customizer_fields_data['wfacp_form']['form_data']['breadcrumb'];

